type TranslationKey = keyof typeof TRANSLATIONS.en;

let runtimeLanguage: AppLanguage = 'pl';

export function setRuntimeLanguage(language: AppLanguage): void {
  runtimeLanguage = language;
}

export function t(
  language: AppLanguage,
  key: TranslationKey,
  params?: TranslationParams,
): string {
  const template = TRANSLATIONS[language][key] ?? TRANSLATIONS.en[key];
  return interpolate(template, params);
}

export function rt(key: TranslationKey, params?: TranslationParams): string {
  return t(runtimeLanguage, key, params);
}

export function localeForLanguage(language: AppLanguage): string {
  return language === 'de' ? 'de-DE' : language === 'en' ? 'en-US' : 'pl-PL';
}

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}
